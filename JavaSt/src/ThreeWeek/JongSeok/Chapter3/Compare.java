package ThreeWeek.JongSeok.Chapter3;
public class Compare {

	public static void main(String[] args) {
		// >, <, >=, <=. ==. !=
		int a, b, c, d;
		a = 1;
		b = 2;
		c = 2;
		d = 3;
		System.out.println(a > b);
		System.out.println(a <= b);
		System.out.println(b == c);
		System.out.println(c == d);
		System.out.println(b != c);
		System.out.println(c != d);
	
	}

}
