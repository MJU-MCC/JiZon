package TwoWeek.Inyoung.March14;

public class Chapter2_04 {
    public static void main(String[] args) {
        int x = 100;        // 정수를 저장할 변수 타입 int
        double pi = 3.14;   // 실수를 저장할 변수 타입 double
        char ch = 'a';      // 문자 1개를 저장할 변수 타입 char
        String str = "abc"; // 여러 문자를 저장할 변수 타입 String

        System.out.println(x);
        System.out.println(pi);
        System.out.println(ch);
        System.out.println(str);
    }
}
