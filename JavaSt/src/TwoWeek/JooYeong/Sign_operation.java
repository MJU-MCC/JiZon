package TwoWeek.JooYeong;

public class Sign_operation {
    public static void main(String[] args) {
        
        //3-06. 부호연산자

        int i = -10;
        i = +i;         //부호 연산자 '+'는 하는 일이 없으며, 쓰이는 경우도 거의 없다
        System.out.println(i);

        i = -10;
        i = -1;         //부호 연산자 '-'를 대체로 사용한다
        System.out.println(i);
    }
}
