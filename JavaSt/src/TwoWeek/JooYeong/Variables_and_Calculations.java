package TwoWeek.JooYeong;

public class Variables_and_Calculations {
    
    //02. 덧셈 뺄셈 계산하기 / 03. 변수의 선언과 저장
    public static void main(String[] args) {
        int a = 10; //int형 a 변수 선언, a에 10저장
        int b = 3; //int형 b 변수 선언, b에 3저장

        int sum = a+b; //int 형 sum 변수 선언, sum에 a+b저장 / sum = 13
        int minus = a-b; //int 형 minus 변수 선언, minus에 a-b저장 / minus = 7

        System.out.println(sum);
        System.out.println(minus);
    }
}
