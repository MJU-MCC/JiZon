package TwoWeek.Jooyeon;

public class Variable_Declaration_Assignment {
    public static void main(String[] args) {
        System.out.println("----------------------------------------------------");
        System.out.println("변수의 선언과 저장");
        System.out.println();

        int a; //int형 변수 a 선언
        a=25; // 변수 a에 25 저장 및 초기화

        System.out.println("a값은:"+ a); // a값 출력
        //+는 문자열과 숫자를 연결하여 문자열로 출력해주는 역할 수행

        int b = 23; // 위의 과정을 한 줄로
        System.out.println("b값은:"+b); // b값 출력
        System.out.println("----------------------------------------------------");
    }
}
