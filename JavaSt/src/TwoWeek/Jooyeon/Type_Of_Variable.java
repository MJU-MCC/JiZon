package TwoWeek.Jooyeon;

public class Type_Of_Variable {
    public static void main(String[] args) {
        System.out.println("----------------------------------------------------");
        System.out.println("변수의 타입");
        System.out.println();

        // 자료형은 값의 종류에 따라 값이 저장될 공간의 크기와 저장형식을 정의한 것
        // 자료형으로 문자형(char), 정수형(byte, short, int, long), 실수형 (float, double) 등이 있다.
        int x =1; // 정수형 변수 x에 정수 1 저장
        char y ='z'; //문자형 변수 y에 'z' 저장
        double z = 2.42; // 실수형 변수 z에 2.42 저장

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println("----------------------------------------------------");

    }
}
