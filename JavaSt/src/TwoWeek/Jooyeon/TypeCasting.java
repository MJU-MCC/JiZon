package TwoWeek.Jooyeon;

public class TypeCasting {
    public static void main(String[] args) {
        /*
        형변환 - 변수 또는 상수의 타입을 다른 타입으로 변환하는 것
        형변환 방법 :  (타입) 피연산자
         */
        double d = 85.4; //double형 변수 d에 85.4 저장
        int score = (int)d; // double 타입의 변수 d를 int 타입으로 형변환
        System.out.println("----------------------------------------------------");
        System.out.println("형변환");
        System.out.println();
        System.out.println("d의 값:"+ d);
        System.out.println("score의 값:"+ score);
        System.out.println("----------------------------------------------------");

    }
}
